export function EmojiPicker({ onSelect }: { onSelect: (emoji: string) => void }) {
  const [activeCategory, setActiveCategory] = useState(0);

  return (
    <div className="flex h-72 w-72 flex-col overflow-hidden rounded-xl border border-[var(--border)] bg-[var(--bg-primary)] shadow-xl">
      {/* 類別 tab 列 */}
      <div className="flex shrink-0 border-b border-[var(--border)] bg-[var(--bg-primary)]">
        {CATEGORIES.map((cat, idx) => (
          <button
            key={cat.label}
            type="button"
            onClick={() => setActiveCategory(idx)}
            title={cat.label}
            className={cn(
              "flex flex-1 items-center justify-center py-2 text-base transition-colors",
              activeCategory === idx
                ? "border-b-2 border-[var(--accent)] bg-[var(--accent-bg)]"
                : "hover:bg-[var(--bg-secondary)]",
            )}
          >
            {cat.icon}
          </button>
        ))}
      </div>

      {/* emoji 格 */}
      <div className="flex-1 overflow-y-auto p-1.5">
        <div className="grid grid-cols-8 gap-0.5">
          {CATEGORIES[activeCategory].emojis.map((emoji) => (
            <button
              key={emoji}
              type="button"
              onClick={() => onSelect(emoji)}
              className="flex items-center justify-center rounded p-1 text-xl leading-none transition-colors hover:bg-[var(--bg-secondary)]"
              aria-label={emoji}
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  );
}
